# Írjon egy programot, ami kiszámolja egy derékszögű parallelepipedon térfogatát, aminek 
# meg van adva a szélessége, a magassága és a hosszúsága.

print('Ez a program kiszámolja egy derékszögű parallelepipedon térfogatát.')
a=int(input('Magasság: '))
b=int(input('Szélesség: '))
c=int(input('Hosszúság: '))
result=a*b*c
print('Térfogat: {}'.format(result))
