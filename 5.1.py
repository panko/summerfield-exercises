# Írjon egy programot, ami a kiindulásul fokokban, percekben és másodpercekben megadott 
# szögeket radiánba számolja át.
import math
rad=math.pi/180
a=float(input('Adj meg egy szöget: '))
b=float(input('Adj meg egy szögpercet: '))
c=float(input('Adj meg egy szögmásdopercet: '))

szogmin=c/60
szog=szogmin+b/60
result=(szog+a)*rad

print('{} radián'.format(result))
