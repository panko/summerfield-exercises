# Definiáljon  egy karakterSor(n, ca) függvényt, ami n darab ca karakterből álló stringet ad vissza.

def karakterSor(n, ca):
	'''
	n -- kimeneti karakterszám
	ca -- kimeneti karakter
	'''
	return str(n*ca)

